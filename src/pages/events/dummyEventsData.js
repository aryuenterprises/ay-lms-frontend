// Helper function to get event by ID
export const getEventById = (id) => {
  return dummyEventsData.find((event) => event.eventId === id);
};

// Helper function to filter events by status
export const getEventsByStatus = (status) => {
  return dummyEventsData.filter((event) => event.status === status);
};

// Helper function to get upcoming events
export const getUpcomingEvents = () => {
  const today = new Date().toISOString().split('T')[0];
  return dummyEventsData.filter((event) => event.eventDate >= today);
};

// Helper function to get past events
export const getPastEvents = () => {
  const today = new Date().toISOString().split('T')[0];
  return dummyEventsData.filter((event) => event.eventDate < today);
};

// Helper function to search events
export const searchEvents = (query) => {
  const lowerQuery = query.toLowerCase();
  return dummyEventsData.filter(
    (event) =>
      event.eventName.toLowerCase().includes(lowerQuery) ||
      event.eventDescription.toLowerCase().includes(lowerQuery) ||
      event.createdBy.toLowerCase().includes(lowerQuery)
  );
};
